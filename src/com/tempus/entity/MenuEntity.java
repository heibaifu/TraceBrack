package com.tempus.entity;

public class MenuEntity {

	private int Icion;
	private String name;
	private int type;
	
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getIcion() {
		return Icion;
	}
	public void setIcion(int icion) {
		Icion = icion;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
